/*relation among operands(<,>,<=,>=,==,!=)
 * result is boolean
 */
public class relationalop {
    public static void main(String args[]){
        int a=40,b=8;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);


    }
    
}
