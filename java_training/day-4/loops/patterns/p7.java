public class p7 {
    public static void main(String args[]){
        int r,s,n=5;
        for(r=1;r<=n;r++){
            for(s=1;s<=n;s++){
                if(s>r){
                      System.out.print("*  ");
                }
                else{
                    System.out.println(" ");
                }

            }
            System.out.println(" ");
        }

    }
}
